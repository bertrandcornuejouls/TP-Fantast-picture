package entite;



public class Images {

	private String nom;
	private String url;
	
	public Images(String nom, String url) {
		this.nom = nom;
		this.url = url;
	}
	
	public String getNom() {
		return this.nom;
	}
	
	public String getUrl() {
		return this.url;
	}
	
	public void setNom(String nom) {
		this.nom = nom;
	}
	
	public void setUrl(String url) {
		this.url = url;
	}
	
	
	
	
	
}
